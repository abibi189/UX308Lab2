// This program *converts* **kilometres** to **miles**

let kilometres = 5;

let miles = kilometres * 0.621371;

console.log(`${kilometres} km is ${miles} miles`)

// This program *converts* **numeric grade** to **letter grade**

let numericgrade = 75;

let lettergrade = 'C'

console.log(numericgrade + " is " + lettergrade);

// This program *calculate* **volume** of a **cylinder**

let diameter = 5;

let height = 2;

let radius = diameter / 2;

let volume = Math.PI * radius * radius * height;

console.log("Volume is " + volume);